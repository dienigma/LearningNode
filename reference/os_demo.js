const os = require('os');

// Platform

console.log(os.platform());

// CPU Arch

console.log(os.arch());

//CPU core info

console.log(os.cpus());

// Memory - Free memory

console.log(os.freemem());

// Memory - Total memory

console.log(os.totalmem());

// Get Home directory

console.log(os.homedir());

// Uptime for os

console.log(os.uptime());